export interface LocationCompactInterface {
  /**
   * Location ID
   */
  id: string;

  /**
   * Location name
   */
  name: string;

  /**
   * Abbreviation
   */
  abbreviation: string;

  /**
   * Location path name, ex: Abu Dhabi, Khalifa City
   */
  path_name: string;

  /**
   * Location path, ex: 1.50.280
   */
  path: string;
}
